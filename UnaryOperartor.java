
public class UnaryOperartor {
    public static void main(String... args) {
        int a = 5, b = 8;
        System.out.println("Increment Operation\n\n");

        System.out.println("Value of a : " + a);
        System.out.println("Value of a++ : " + a++);
        System.out.println("Value of a : " + a);

        System.out.println("Value of b : " + b);
        System.out.println("Value of b++ : " + b++);
        System.out.println("Value of b : " + b);
        System.out.println("Value of a : " + a);
        System.out.println("Value of ++a : " + ++a);
        System.out.println("Value of a : " + a);

        System.out.println("Value of b : " + b);
        System.out.println("Value of ++b : " + ++b);
        System.out.println("Value of b : " + b);

        System.out.println("Decrement Operation\n\n");

        System.out.println("Value of a : " + a);
        System.out.println("Value of a-- : " + a--);
        System.out.println("Value of a : " + a);

        System.out.println("Value of b : " + b);
        System.out.println("Value of b-- : " + b--);
        System.out.println("Value of b : " + b);

        System.out.println("Value of a : " + a);
        System.out.println("Value of --a : " + --a);
        System.out.println("Value of a : " + a);

        System.out.println("Value of b : " + b);
        System.out.println("Value of --b : " + --b);
        System.out.println("Value of b : " + b);
    }

}