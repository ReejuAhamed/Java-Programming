
public class ShiftOperator {
    public static void main(String[] args) {
        int a = 20;
        System.out.println(a << 2); /// a * 2^2 = 20 *4 = 80
        System.out.println(a >> 2); /// a / 2^2 = 20 / 4 = 5

        System.out.println(a << 4); /// a * 2^4 = 20 *16 = 320
        System.out.println(a >> 4); /// a / 2^4 = 20 / 16 = 1......

    }

}